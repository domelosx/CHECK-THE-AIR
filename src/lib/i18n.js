export function getLang() {
    return localStorage.getItem(KEY) || "pl";
}

export function setLang(code) {
    localStorage.setItem(KEY, code);
    window.dispatchEvent(new CustomEvent("scba-lang-change"));
}

export function translate(lang, key, ...args) {
    const dict = T[lang] || T.pl;
    const v = dict[key];
    if (typeof v === "function") return v(...args);
    if (v !== undefined) return v;
    const fallback = T.pl[key];
    if (typeof fallback === "function") return fallback(...args);
    return fallback !== undefined ? fallback : key;
}

export function useLang() {
    const [lang, setLangState] = useState(getLang());
    useEffect(() => {
        const onChange = () => setLangState(getLang());
        window.addEventListener("scba-lang-change", onChange);
        window.addEventListener("storage", onChange);
        return () => {
            window.removeEventListener("scba-lang-change", onChange);
            window.removeEventListener("storage", onChange);
        };
    }, []);
    const t = (key, ...args) => translate(lang, key, ...args);
    return { lang, setLang, t };
}
